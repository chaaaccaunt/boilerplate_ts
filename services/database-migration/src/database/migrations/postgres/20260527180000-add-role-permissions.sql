CREATE TABLE IF NOT EXISTS "permissions" (
  "uid" UUID NOT NULL,
  "key" VARCHAR(128) NOT NULL,
  "title" VARCHAR(128) NOT NULL,
  "description" TEXT NULL,
  "createdAt" TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  "updatedAt" TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  "deletedAt" TIMESTAMP NULL,
  PRIMARY KEY ("uid"),
  CONSTRAINT "permissions_key_unique" UNIQUE ("key")
);

CREATE TABLE IF NOT EXISTS "role_permissions" (
  "uid" UUID NOT NULL,
  "roleUid" UUID NOT NULL,
  "permissionUid" UUID NOT NULL,
  "createdAt" TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  "updatedAt" TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  "deletedAt" TIMESTAMP NULL,
  PRIMARY KEY ("uid"),
  CONSTRAINT "role_permissions_role_permission_unique" UNIQUE ("roleUid", "permissionUid")
);

CREATE INDEX IF NOT EXISTS "role_permissions_permission_uid_index" ON "role_permissions" ("permissionUid");

